-- Used by the app right after a JSON backup is restored. Backup restores
-- re-insert rows with their original IDs, which the auto-increment counters
-- don't know about — this resets each counter to (max id + 1) so the next
-- manually-added record can't collide with a restored one.
create or replace function fix_all_sequences()
returns void
language plpgsql
security definer
as $$
begin
  perform setval(pg_get_serial_sequence('app_settings', 'id'), coalesce((select max(id) from app_settings), 1));
  perform setval(pg_get_serial_sequence('contributors', 'id'), coalesce((select max(id) from contributors), 1));
  perform setval(pg_get_serial_sequence('collections', 'id'), coalesce((select max(id) from collections), 1));
  perform setval(pg_get_serial_sequence('collection_months', 'id'), coalesce((select max(id) from collection_months), 1));
  perform setval(pg_get_serial_sequence('transfers', 'id'), coalesce((select max(id) from transfers), 1));
  perform setval(pg_get_serial_sequence('balance_adjustments', 'id'), coalesce((select max(id) from balance_adjustments), 1));
  perform setval(pg_get_serial_sequence('viewer_activity', 'id'), coalesce((select max(id) from viewer_activity), 1));
end;
$$;

grant execute on function fix_all_sequences() to anon, authenticated;
